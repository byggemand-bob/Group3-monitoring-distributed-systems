package com.group3.monitorClient.util;

public class MonitorUtils {

	/**
	 * Gets the name of the method.
	 * 
	 * @param elementIndex The index used to lookback into the StackTrace to get the name of a calling method.
	 * @return The name of the calling method at the given index.
	 */
	public static String GetCallingMethodName(int elementIndex) {
		StackTraceElement[] stackTrace = Thread.currentThread().getStackTrace();
		StackTraceElement element = stackTrace[elementIndex];
		return element.getMethodName();
	}
}
